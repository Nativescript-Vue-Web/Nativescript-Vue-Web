<template>
    <label class="switch">
        <input
          ref="switcher"
          type="checkbox"
          :checked="value"
          @change="onCheckedChange($event)"
          @click="updateValue()"
        />
        <span class="slider" />
    </label>
</template>

<script>
export default {
    name: 'Switcher',
    props: [
        'checkedChange',
        'value',
    ],
    methods: {
        updateValue: function () {
            this.$emit('input', this.$refs.switcher.checked);
        },
        onCheckedChange: function (event) {
            this.$emit('checkedChange', event);
        },
    },
};
</script>

