<template>
    <div class="slidecontainer">
        <input
            ref="slider"
            class="slider"
            type="range"
            :min="minValue"
            :max="maxValue"
            :value="value"
            @input="updateValue()"
            @change="onValueChange($event)"
        />
    </div>
</template>

<script>
export default {
  name: 'Slider',
  props: [
    'minValue',
    'maxValue',
    'valueChange', // an event.
    'value',
  ],
  methods: {
    updateValue: function() {
      this.$emit('input', this.$refs.slider.value);
    },
    onValueChange: function(event) {
      this.$emit('valueChange', event);
    },
  },
};
</script>

